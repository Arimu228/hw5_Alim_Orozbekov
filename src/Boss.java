public class Boss {
    private int bossDamage;
    private int bossHealth;
    private String bossTypeOfDefence;


    public int getBossDamage() {
        return bossDamage;
    }

    public void setBossDamage(int bossDamage) {
        this.bossDamage = bossDamage;
    }

    public int getBossHealth() {
        return bossHealth;
    }

    public void setBossHealth(int bossHealth) {
        this.bossHealth = bossHealth;
    }

    public String getBossTypeOfDefence() {
        return bossTypeOfDefence;
    }

    public void setBossTypeOfDefence(String bossTypeOfDefence) {
        this.bossTypeOfDefence = bossTypeOfDefence;
    }



}
